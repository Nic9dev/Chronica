def get_frame(state: str, frame: int = 0) -> List[List[int]]:
    """
    状態とフレーム番号からピクセルアートデータを取得
    
    Args:
        state: "idle", "thinking", "action"
        frame: フレーム番号（idleの場合のみ有効）
    
    Returns:
        ピクセルアートのグリッドデータ
    """
    if state == "idle":
        if frame % 2 == 0:
            return SUI_IDLE["frame1"]
        else:
            return SUI_IDLE["frame2"]
    elif state == "thinking":
        return SUI_THINKING
    elif state == "action":
        return SUI_ACTION
    else:
        return SUI_IDLE["frame1"]
